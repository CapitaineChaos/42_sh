#include <limits.h>

void	ft_approx_sleep(int ms)
{
	int	i;
	int	j;

	if (ms < 0)
		return ;
	if (ms > INT_MAX / 2300)
		ms = INT_MAX / 2300;
	j = 0;
	while (j < ms)
	{
		i = 0;
		while (i < (INT_MAX / 2300))
		{
			i++;
		}
		j++;
	}
}
